  let lang = localStorage.getItem(STORAGE_KEY) || 'en';

  function getLang() {
    return lang;
  }

  function setLang(l) {
    if (t[l]) {
      lang = l;
      localStorage.setItem(STORAGE_KEY, l);
      document.documentElement.lang = l === 'uk' ? 'uk' : 'en';
      document.documentElement.classList.remove('lang-en', 'lang-uk');
      document.documentElement.classList.add('lang-' + l);
      apply();
      if (typeof window.onLangChange === 'function') window.onLangChange(l);
    }
  }

  function tr(key) {
    return t[lang]?.[key] ?? t.en[key] ?? key;
  }

  function apply() {
    const title = tr('pageTitle');
    if (title) document.title = title;
    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.getAttribute('data-i18n');
      const val = tr(key);
      if (val) el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      const key = el.getAttribute('data-i18n-placeholder');
      const val = tr(key);
      if (val) el.placeholder = val;
    });
    document.querySelectorAll('[data-i18n-title]').forEach((el) => {
      const key = el.getAttribute('data-i18n-title');
      const val = tr(key);
      if (val) el.title = val;
    });
    document.querySelectorAll('[data-i18n-html]').forEach((el) => {
      const key = el.getAttribute('data-i18n-html');
      const val = tr(key);
      if (val) el.innerHTML = val;
    });
    document.querySelectorAll('[data-i18n-opt]').forEach((el) => {
      const key = el.getAttribute('data-i18n-opt');
      const val = tr(key);
      if (val) el.textContent = val;
    });
  }

  window.CL = window.CL || {};
  window.CL.i18n = {
    t: tr,
    getLang,
    setLang,
    apply,
    keys: Object.keys(t.en),
  };

  document.addEventListener('DOMContentLoaded', () => {
    document.documentElement.lang = lang === 'uk' ? 'uk' : 'en';
    document.documentElement.classList.remove('lang-en', 'lang-uk');
    document.documentElement.classList.add('lang-' + lang);
    apply();
  });
